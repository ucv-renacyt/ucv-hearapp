<script>
    document.addEventListener('DOMContentLoaded', (event) => {
        const addTemaBtn = document.getElementById('addTemaBtn');
        const temasContainer = document.getElementById('temasContainer');
        let temaCount = 0;

        addTemaBtn.addEventListener('click', () => {
            temaCount++;
            const newInput = document.createElement('div');
            newInput.classList.add('form-floating', 'mb-3');
            newInput.innerHTML = `
            <input type="text" class="form-control" id="tema${temaCount}" placeholder="Tema ${temaCount}" name="temas[]" required>
            <label for="tema${temaCount}">Tema ${temaCount}</label>
        `;
            temasContainer.appendChild(newInput);
        });
    });
</script>